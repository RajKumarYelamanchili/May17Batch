package iterative_statements;

public class ForEachLoop {

	public static void main(String[] args) {
		
		
		  int arr[] = {34,98,67,19,25,47,1,99,6};
		  System.out.println("Length of the array: " + arr.length);
		  
		  
		  for(int a :arr)
		  { 
			  System.out.println(a); 
		  }
		 
		
		
		

	}

}
