package operators;

public class PrePostIncrement {

	public static void main(String[] args) {
		//pre increment
		int i=5;
		int j=++i;
		System.out.println(i);//6
		System.out.println(j);//6
		
		
		
		
		
		
	System.out.println("*********************");
		//post increment
		int x=5;
		int y=x++;
		
		
		

	}

}
