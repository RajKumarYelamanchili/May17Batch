package selection_statements;

public class IfElse {

	public static void main(String[] args) {
		
		int a=2;
		int b=4;
		
		if (a+b <6)
		{
			System.out.println("a+b is less than 6");
		}
		else
		{
			System.out.println("a+b is greater than or equal to 6");
		}
		
		
	}

}
